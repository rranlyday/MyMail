package model;

public class OrderRecive {
    private Integer id;

    private Integer mailId;

    private Integer mailmanId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMailId() {
        return mailId;
    }

    public void setMailId(Integer mailId) {
        this.mailId = mailId;
    }

    public Integer getMailmanId() {
        return mailmanId;
    }

    public void setMailmanId(Integer mailmanId) {
        this.mailmanId = mailmanId;
    }
}